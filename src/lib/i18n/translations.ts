/**
 * Translation hook
 */
export function useTranslation(locale: 'en' | 'es' = 'en') {
    const translations = locale === 'en' ? enTranslations : esTranslations;

    const t = (key: string): string => {
        return translations[key as keyof typeof translations] || key;
    };

    return { t, locale };
}
